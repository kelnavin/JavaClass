/* Example - ELSE IF  */

public class Percentage{
    public static void main(String[] args) {
        double P =90;
        if (P>=80){

            System.out.println("Dist");
        }
        else if (P>=60){
            System.out.println("1st Div");
        }
        else if (P>=45){
            System.out.println("2nd Div");
        }
        else if (P>=35){
            System.out.println("3rd Div");
        }
        else {
            System.out.println("Fail");
        }
    }


}

